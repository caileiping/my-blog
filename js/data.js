// 获取所有标签
function getAllTags() {
    const tags = new Set();
    blogData.posts.forEach(post => {
        post.tags.forEach(tag => tags.add(tag));
    });
    return Array.from(tags);
}

// 根据标签筛选文章
function getPostsByTag(tag) {
    if (tag === 'all') return blogData.posts;
    return blogData.posts.filter(post => post.tags.includes(tag));
}

// 搜索文章
function searchPosts(keyword) {
    const lowerKeyword = keyword.toLowerCase();
    return blogData.posts.filter(post => {
        return post.title.toLowerCase().includes(lowerKeyword) ||
               post.excerpt.toLowerCase().includes(lowerKeyword) ||
               post.tags.some(tag => tag.toLowerCase().includes(lowerKeyword));
    });
}

// 获取单篇文章
function getPostById(id) {
    return blogData.posts.find(post => post.id === id);
}

// 获取文章评论
function getComments(postId) {
    const stored = localStorage.getItem(`comments_${postId}`);
    if (stored) {
        return JSON.parse(stored);
    }
    return blogData.comments[postId] || [];
}

// 添加评论
function addComment(postId, comment) {
    const comments = getComments(postId);
    comment.id = Date.now();
    comment.date = new Date().toISOString().split('T')[0];
    comments.push(comment);
    localStorage.setItem(`comments_${postId}`, JSON.stringify(comments));
    return comments;
}
